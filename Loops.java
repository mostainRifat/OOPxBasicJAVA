public class Loops {
    public static void main(String[] args) {
        //for
        for(int i = 1 ; i<= 10;i++)
        {
            System.out.println(i);
        }
        //While
        int j = 10;
        while(j>0)
        {
            System.out.println(j);
            j--;
        }
        //Do-While
        int k = 10;
        do{
            System.out.println(k);
            k++;
        } while(k <= 20);
    }
}
