public class Operators {
    public static void main(String[] args) {
        int a = 31;
        int b = 20;
        int sum = a + b;
        int diff = a - b;
        System.out.println(sum);
        System.out.println(diff);

        double modulo = a%b;
        System.out.println(modulo);

        // '=' is assignment operators
        // '++' and '--' unary operators
    }

}